# Creation d'une liste de 5 nombres.
number = [1 ,2 ,3 ,4 ,1 ,0 ,8 ,7 ,4]

# Ajout d'un element à la fin
number.append(12)

# Ajout d'un element au debut

number.insert(0, 5)
print(number)

# Suppression du troisième element
number.remove(2)
print(number)

# Trier le tableau de façon croissant
number.sort()
print(number)